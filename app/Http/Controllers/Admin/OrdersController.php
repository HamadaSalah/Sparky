        return redirect()->back()->with('success', 'User Deleted Successfully');
    }

    public function UpdateUserStatus(Request $request) {
        $user = Order::findOrfail($request->id);
        if($user->isVerified == true) {
            $user->update([
                'isVerified' => false
            ]);
        }
        else {
            $user->update([
                'isVerified' => true
            ]);
        }
    }
